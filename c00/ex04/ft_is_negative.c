#include <unistd.h>

void	ft_is_negative(int	n)
{
	char	i;
	char	y;

	if (n < 0)
	{
		i = 'N';
		write(1, &i, 1);
	}
	else
	{
		y = 'P';
		write(1, &y, 1);
	}
}
